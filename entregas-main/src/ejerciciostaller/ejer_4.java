    public static String conversion(int num) {
        String[] unidades = {"cero", "uno", "dos", "tres", "cuatro", "cinco", "seis", "siete", "ocho", "nueve"};
        String[] decenas = {"diez", "once", "doce", "trece", "catorce", "quince", "dieciseis", "diecisiete", "dieciocho", "diecinueve"};
        String[] centenas = {"", "", "veinte", "treinta", "cuarenta", "cincuenta", "sesenta", "setenta", "ochenta", "noventa"};
        if (num < 10) {
            return unidades[num];
        } else if (num >= 10 && num < 20) {
            return decenas[num - 10];
        } else {
            int decena = num / 10;
            int unidad = num % 10;
            String resultado = decenas[decena];
            if (unidad > 0) {
                resultado += " y " + unidades[unidad];
            }
            return resultado;
        }
    }

