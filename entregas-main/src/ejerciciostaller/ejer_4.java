    public static int cadena(String frase) {
        if (frase == null || frase.isEmpty()) {
            return 0;
        }
        String[] num1 = frase.trim().split("\\s+");
        return num1.length;
    }

    public static String capitalizar(String frase) {
        if (frase == null || frase.isEmpty()) {
            return "";
        }
        String[] caracteres = frase.trim().split("\\s+");
        StringBuilder charter = new StringBuilder();
        for (String palabra : caracteres) {
            char linea = Character.toUpperCase(palabra.charAt(0));
            charter.append(linea).append(palabra.substring(1)).append(" ");
        }
        return charter.toString().trim();
    }

