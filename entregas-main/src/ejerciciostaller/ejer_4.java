    public static boolean capicua(int numero12) {
        int numeroveces = 0;
        int digito1 = numero12;

        while (numero12 != 0) {
            int digito = numero12 % 10;
            numeroveces = numeroveces * 10 + digito;
            numero12 /= 10;
        }
        return digito1 == numeroveces;
    }

