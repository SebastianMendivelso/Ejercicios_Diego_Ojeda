    public static boolean Palindroma(String digito) {
        digito = digito.replaceAll("", "").toLowerCase();
        int a = 0;
        int b = digito.length() - 1;
        while (a < b) {
            if (digito.charAt(a) != digito.charAt(b)) {
                return false;
            }
            a++;
            b--;
        }
        return true;
    }

